#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*new_str;
	int		i;

	new_str = (char *)malloc(sizeof(char) * ft_strlen(src) + 1);
	if (new_str == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		new_str[i] = src[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}

#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list *new;

	if ((new = (t_list *)malloc(sizeof(t_list))) == NULL)
		return (NULL);
	new->next = NULL;
	if (content == NULL)
	{
		new->content = NULL;
		new->content_size = 0;
		return (new);
	}
	if ((new->content = ft_strdup((char*)content)) == NULL)
	{
		free(new);
		return (NULL);
	}
	new->content_size = content_size;
	return (new);
}
